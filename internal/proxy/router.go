func renderErrorPage(status int, title, body string) string {
	return fmt.Sprintf(
		"<html><head><title>%d — %s</title><style>%s</style></head><body><h1>%d — %s</h1>%s</body></html>",
		status, title, errorPageCSS, status, title, body)
}

func (r *Router) extractSubdomain(host string) string {
	return extractSubdomainFor(host, r.baseDomain)
}

// extractSubdomainFor returns the subdomain portion of a host like
// "salt-main.localhost" → "salt-main". Returns "" for plain base domain.
func extractSubdomainFor(host, baseDomain string) string {
	host = strings.ToLower(host)
	base := strings.ToLower(baseDomain)
	if host == base || host == "127.0.0.1" || host == "::1" {
		return ""
	}
	suffix := "." + base
	if strings.HasSuffix(host, suffix) {
		return strings.TrimSuffix(host, suffix)
	}
	return ""
}

var branchPrefixRe = regexp.MustCompile(`^(feature|chore|bugfix|hotfix)/`)
var sanitizeRouteRe = regexp.MustCompile(`[^a-z0-9-]`)
var collapseDashRe = regexp.MustCompile(`-{2,}`)

// RouteKey derives a DNS-safe route key from a project name and branch.
// Exported so gtl setup can display the router URL.
func RouteKey(project, branch string) string {
	project = strings.ToLower(project)
	project = sanitizeRouteRe.ReplaceAllString(strings.ReplaceAll(project, "_", "-"), "")

	if branch == "" {
		return truncateDNSLabel(project)
	}

	branch = strings.ToLower(branch)
	branch = branchPrefixRe.ReplaceAllString(branch, "")
	branch = strings.ReplaceAll(branch, "/", "-")
	branch = strings.ReplaceAll(branch, "_", "-")
	branch = sanitizeRouteRe.ReplaceAllString(branch, "")
	branch = collapseDashRe.ReplaceAllString(branch, "-")
	branch = strings.Trim(branch, "-")

	return truncateDNSLabel(project + "-" + branch)
}

// BuildRouterURL returns the HTTPS router URL for a given project/branch
// when the router is active, or falls back to a plain localhost URL.
// Pass svcRunning and pfConfigured from service.IsRunning() and
// service.IsPortForwardConfigured() to keep this function testable.
func BuildRouterURL(port int, project, branch, domain string, routerPort int, svcRunning, pfConfigured bool) string {
	if branch != "" && svcRunning {
		routeKey := RouteKey(project, branch)
		if pfConfigured {
			return fmt.Sprintf("https://%s.%s", routeKey, domain)
		}
		return fmt.Sprintf("https://%s.%s:%d", routeKey, domain, routerPort)
	}
	return fmt.Sprintf("http://localhost:%d", port)
}

// BuildTunnelURL returns the tunnel URL for a given project/branch when
// a tunnel domain is configured, or an empty string if not applicable.
func BuildTunnelURL(project, branch, tunnelDomain string) string {
	if tunnelDomain == "" || branch == "" {
		return ""
	}
	return "https://" + RouteKey(project, branch) + "." + tunnelDomain
}

const maxDNSLabel = 63

func truncateDNSLabel(label string) string {
	if len(label) <= maxDNSLabel {
		return label
	}
	h := sha256.Sum256([]byte(label))
	suffix := hex.EncodeToString(h[:])[:6]
	prefix := label[:maxDNSLabel-7]
	prefix = strings.TrimRight(prefix, "-")
	return prefix + "-" + suffix
}

var localhostCache sync.Map // port (int) → localhostEntry

type localhostEntry struct {
	host    string
	expires time.Time
}

const localhostCacheTTL = 5 * time.Second

// resolveLocalhost checks whether a port is reachable on IPv4 (127.0.0.1)
// or IPv6 (::1) and returns the appropriate address. Results are cached for
// localhostCacheTTL to avoid probing on every proxied request.
func resolveLocalhost(port int) string {
	if v, ok := localhostCache.Load(port); ok {
		if e := v.(localhostEntry); time.Now().Before(e.expires) {
			return e.host
		}
	}
	host := probeLocalhost(port)
	localhostCache.Store(port, localhostEntry{host: host, expires: time.Now().Add(localhostCacheTTL)})
	return host
}

func probeLocalhost(port int) string {
	for _, host := range []string{"127.0.0.1", "::1"} {
		addr := net.JoinHostPort(host, fmt.Sprintf("%d", port))
		conn, err := net.DialTimeout("tcp", addr, 500*time.Millisecond)
		if err == nil {
			_ = conn.Close()
			return host
		}
	}
	return "127.0.0.1"
}

func sortedKeys(m map[string]int) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
